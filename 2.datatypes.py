# Strings or str
print(type("Hello World"))
print(type('Hello World'))
print(type("""Hello World"""))
print(type('''Hello World'''))

# Numbers (Integers or Floats)
print(type(100)) # Int
print(type(4.5)) # Float


# Boolean
print(type(True)) # Verdadero
print(type(False)) # Falso

# List
[20, 30, 55]
["HELLO", 'bye', 'good']
[10, 4.5, "hello", True]
[] # List void.


# Tuples
(10, 20, 30, 55)
() # Tuple void.


